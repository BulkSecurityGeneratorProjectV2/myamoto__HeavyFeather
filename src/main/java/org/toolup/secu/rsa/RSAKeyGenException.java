package org.toolup.secu.rsa;

public class RSAKeyGenException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8354430557570504242L;

	
	public RSAKeyGenException(String msg) {
		super(msg);
	}
	

	public RSAKeyGenException(Throwable t) {
		super(t);
	}
	
	public RSAKeyGenException(String msg, Throwable t) {
		super(msg, t);
	}
	
	
}
